SELECT * FROM Орендатор ORDER BY [Ім'я] ASC

SELECT [Назва жанру], COUNT(Орендатор.[Код орендатора])as [Вибраний жанр]
FROM Орендатор, [Прокат фільму], [Жанр фільму] WHERE 
Орендатор.[Код орендатора] = [Прокат Фільму].[Код прокату]
AND [Жанр фільму].[Код жанру]=[Прокат фільму].[Код прокату]
GROUP BY [Назва жанру]

